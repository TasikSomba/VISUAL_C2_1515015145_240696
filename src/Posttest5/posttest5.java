    private void resetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetActionPerformed
        // TODO add your handling code here:
        nim.setText("");
        nama.setText("");
        angkatan.setSelectedItem(null);
        kota.setSelectedItem(null);        
        nim.requestFocus();
    }//GEN-LAST:event_resetActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
         try {
        int baris = jTable1.getSelectedRow();
        jTable1.setValueAt(nama.getText(), baris, 0);
        jTable1.setValueAt(nim.getText(), baris, 1);
        jTable1.setValueAt(angkatan.getSelectedItem(), baris, 2);    
        jTable1.setValueAt(kota.getSelectedItem(), baris, 3);  
        } catch (Exception e) {
        }
    }//GEN-LAST:event_updateActionPerformed

    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        // TODO add your handling code here:
       try {
            int pilih = JOptionPane.showConfirmDialog(this,"ingin hapus","Confirmasi hapus ",JOptionPane.YES_OPTION);
        if(pilih==JOptionPane.YES_OPTION){
        int baris = jTable1.getSelectedRow();
        model.removeRow(baris);}
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Tologn pilih data yang ingin dihapus");
        }
    }//GEN-LAST:event_hapusActionPerformed

