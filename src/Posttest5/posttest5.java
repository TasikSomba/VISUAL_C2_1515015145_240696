    private void btMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btMouseClicked
        // TODO add your handling code here:
        int pilih = JOptionPane.showConfirmDialog(this,"ingin Berishkan tabel ","Confirmasi hapus ",JOptionPane.YES_OPTION);
        if(pilih==JOptionPane.YES_OPTION){
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        nama.requestFocus();}
    }//GEN-LAST:event_btMouseClicked

    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
         Object[] data = new Object[4];
              data[0] = nama.getText();
              data[1] = nim.getText();
              data[2] = angkatan.getSelectedItem().toString();
              data[3] = kota.getSelectedItem().toString();
              model.addRow(data);
        update.setEnabled(true);
        hapus.setEnabled(true);
        reset.setEnabled(true);
        bt.setEnabled(true);
    }//GEN-LAST:event_simpanActionPerformed

    private void namaCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_namaCaretUpdate
        // TODO add your handling code here:
        if (nama.getText().length()== 0 ){
            simpan.setEnabled(false);
        }
        else if (nama.getText().length()!=0 && nim.getText().length()!=0){
            simpan.setEnabled(true);
        }
    }//GEN-LAST:event_namaCaretUpdate

    private void nimCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_nimCaretUpdate
        // TODO add your handling code here:
        if (nim.getText().length()== 0){
            simpan.setEnabled(false);
        }
        else if (nama.getText().length()!=0 && nim.getText().length()!=0){
            simpan.setEnabled(true);
        }
    }//GEN-LAST:event_nimCaretUpdate

    private void nimKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nimKeyPressed
        // TODO add your handling code here:
        nim.addKeyListener(new KeyAdapter() {
            public void keyTyped(KeyEvent e){
            char k = e.getKeyChar();
            if(!((k >= '0') && (k <= '9')) || (k == '\b')){
                e.consume();
            }
            }
            
}); 
    }//GEN-LAST:event_nimKeyPressed

