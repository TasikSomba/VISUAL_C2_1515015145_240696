    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int baris = jTable1.getSelectedRow();
         String nama1 = jTable1.getValueAt(baris,0).toString();
         String nim1 = jTable1.getValueAt(baris,1).toString();
         String kota1 = jTable1.getValueAt(baris,2).toString();
        nama.setText(nama1);
        nim.setText(nim1);
        kota.setSelectedItem(kota1);
        
    }//GEN-LAST:event_jTable1MouseClicked

